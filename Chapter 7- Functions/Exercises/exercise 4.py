def make_shirt(size="large", message="I love Python"):
    print(f"Shirt size: {size.upper()}, Message: {message}")

#MAKING A LARGE shirt with a default message
make_shirt()

# MAKING A Medium shirt with a default message
make_shirt(size="medium")

#Making a custo sized shirt with a default message
make_shirt(size="small", message="Coding is fun!")
